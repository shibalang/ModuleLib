package com.module.beans;

public class ECEntity {
	private String entry;
	private String enzymename;
	private String ecclass;
	private String sysname;
	private String reaction;
	private String substrate;
	private String product;
	private String comment;
	private String pathway;
	private String orthology;
	public String getEntry() {
		return entry;
	}
	public void setEntry(String entry) {
		this.entry = entry;
	}
	public String getEnzymename() {
		return enzymename;
	}
	public void setEnzymename(String enzymename) {
		this.enzymename = enzymename;
	}
	public String getEcclass() {
		return ecclass;
	}
	public void setEcclass(String ecclass) {
		this.ecclass = ecclass;
	}
	public String getSysname() {
		return sysname;
	}
	public void setSysname(String sysname) {
		this.sysname = sysname;
	}
	public String getReaction() {
		return reaction;
	}
	public void setReaction(String reaction) {
		this.reaction = reaction;
	}
	public String getSubstrate() {
		return substrate;
	}
	public void setSubstrate(String substrate) {
		this.substrate = substrate;
	}
	public String getProduct() {
		return product;
	}
	public void setProduct(String product) {
		this.product = product;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
	public String getPathway() {
		return pathway;
	}
	public void setPathway(String pathway) {
		this.pathway = pathway;
	}
	public String getOrthology() {
		return orthology;
	}
	public void setOrthology(String orthology) {
		this.orthology = orthology;
	}
	
}
